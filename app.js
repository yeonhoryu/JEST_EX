/** add function */

function addNumber(number1, number2) {
  return number1 + number2;
}
module.exports = addNumber(number1, number2);


/** subtract function */
function subtractNumber(number1, number2) {
  return number1 - number2;
}
module.exports = subtractNumber(number1, number2);

/** multiple function */
function multiNumber(number1, number2) {
  return number1 * number2;
}
module.exports = multiNumber(number1, number2);

/** divide function */
function dividNumber(number1, number2) {
  return number1 / number2;
}
module.exports = dividNumber(number1, number2);



